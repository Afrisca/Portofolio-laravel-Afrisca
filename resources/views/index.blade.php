$('#contactForm').submit(function(event) {
    event.preventDefault(); // Mencegah pengiriman form default
    let formData = $(this).serialize(); // Mengambil data form

    $.ajax({
        url: '/send-whatsapp',
        method: 'POST',
        data: formData,
        success: function(response) {
            // Mengecek apakah respons mengandung pesan sukses
            if (response.success) {
                // Menampilkan pesan sukses di frontend
                $('#message').removeClass('alert-danger').addClass('alert-success').text(response.message);
            } else {
                // Menampilkan pesan error di frontend jika ada
                $('#message').removeClass('alert-success').addClass('alert-danger').text(response.message);
            }
        },
        error: function(xhr) {
            // Menampilkan pesan error jika ada kesalahan di sisi server
            $('#message').removeClass('alert-success').addClass('alert-danger').text('Error: ' + xhr.responseText);
        }
    });
});

